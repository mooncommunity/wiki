        <h1 class="mb-4">TTT (Trouble in Terrorist Town) Nedir?</h1>
        <hr>
        <p>TTT, sosyal dedektif oyunudur. Oyuncular, Terörist, Dedektif veya Masum rollerinden birine atanır ve
            her grup, diğer grupları yenmeye çalışır. Oyun sırasında iletişim, dikkat ve strateji kritik öneme
            sahiptir.</p>
        <hr>
        <h2>Roller:</h2>
        <ul>
            <li><strong>Masumlar</strong>: Teröristleri bulmaya çalışırlar, ancak kendilerini savunacak
                silahları yoktur.
            </li>
            <li><strong>Dedektifler</strong>: Masumları koruyup, teröristleri ortaya çıkarmak için özel
                yeteneklere sahiptir.
            </li>
            <li><strong>Teröristler</strong>: Masumları ve dedektifleri yok ederek oyunu kazanmayı hedefler.
            </li>
        </ul>
        <hr>
        <h2>Oyun Süreci:</h2>
        <p>Oyun başladığında herkes gizli rolünü öğrenir. Teröristler, masumları öldürmeden önce dedektifleri ve
            diğer teröristleri ortadan kaldırmalıdır. Dedektifler, şüpheli oyuncuları tespit edip masumları
            korumalıdır.</p>
        <br>
        <h3>Stratejiler</h3>
        <br>
        <h4>Masumlar için:</h4>
        <ul>
            <li><strong>Gizli Kal</strong>: Dikkat çekmemeye çalışın, teröristleri bulmaya çalışın.</li>
            <li><strong>Ekip Çalışması</strong>: Diğer masumlarla birlikte hareket edin.</li>
            <li><strong>Soru Sorun</strong>: Diğer oyuncuların davranışlarını gözlemleyin ve sorular sorun.</li>
        </ul>
        <br>

        <h4>Dedektifler için:</h4>
        <ul>
            <li><strong>Donanım Kullanımı</strong>: Dedektif olarak özel eşyalarınızı etkili bir şekilde
                kullanın.
            </li>
            <li><strong>Şüpheli Tespiti</strong>: Oyuncuların davranışlarını analiz edin. Masumlar ve
                teröristler arasındaki farkları görün.
            </li>
            <li><strong>Savunma Yapın</strong>: Masumları korumak için stratejik yerlerde durun.</li>
        </ul>
        <br>

        <h4>Teröristler için:</h4>
        <ul>
            <li><strong>Kandırma</strong>: Diğer oyuncuları manipüle etmeye çalışın. Kendinizi masum gibi
                gösterin.
            </li>
            <li><strong>İşbirliği</strong>: Diğer teröristlerle iletişim kurarak birlikte hareket edin.</li>
            <li><strong>Hızlı Hareket</strong>: Dedektiflerden uzak durun ve hızlı hareket edin.</li>
        </ul>
        <br>
        <hr>
        <h2>Öneriler</h2>
        <ul>
            <li><strong>Sesli İletişim</strong>: Diğer oyuncularla iletişimde bulunmak için sesli chat kullanın;
                bu, stratejik kararlar almanıza yardımcı olabilir.
            </li>
            <li><strong>Harita Bilgisi</strong>: Oynadığınız haritayı iyi öğrenin; bu, kaçış yolları ve saklanma
                yerleri bulmanıza yardımcı olur.
            </li>
            <li><strong>Oyuncu Davranışları</strong>: Diğer oyuncuların alışkanlıklarını gözlemleyin.
                Alışılmadık davranışlar genellikle şüphe uyandırır.
            </li>
        </ul>
