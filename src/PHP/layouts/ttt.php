        <h1 class="standartcolor mb-4">TTT (Trouble in Terrorist Town) Nedir?</h1>
        <p>TTT, sosyal dedektif oyunudur. Oyuncular, Terörist, Dedektif veya Masum rollerinden birine
            atanır ve
            her grup, diğer grupları yenmeye çalışır. Oyun sırasında iletişim, dikkat ve strateji kritik öneme
            sahiptir.</p>
        <br>
        <h2 class="standartcolor">Roller:</h2>
        <ul style="list-style-type: '- '">
            <li><span><strong class="tttmasum">Masumlar</strong>: Dedektifin emirlerine uyarak teröristleri bulmaya hedefler.</span>
            </li>
            <li><span><strong class="tttdedektif">Dedektifler</strong>: Masumları koruyup, teröristleri ortaya çıkarmak için özel
                yeteneklere sahiptir.</span>
            </li>
            <li><span><strong class="ttthain">Teröristler</strong>: Masumları ve dedektifleri yok ederek oyunu kazanmayı hedefler.
            </span></li>
        </ul>
        <br>
        <h2 class="standartcolor">Oyun Süreci:</h2>
        <p>Oyun başladığında herkes gizli rolünü öğrenir. Teröristler, masumları öldürmeden önce
            dedektifleri ortadan
            kaldırmalıdır. Dedektifler, şüpheli oyuncuları tespit edip masumları
            korumalıdır.</p>
        <br>
        <h3 class="standartcolor">Stratejiler</h3>
        <br>
        <h4 class="standartcolor">Masumlar için:</h4>
        <ul style="list-style-type: '— '">
            <li><span><strong class="text-success-emphasis">Gizli Kal</strong>: Dikkat çekmemeye çalışın, teröristleri bulmaya çalışın.</span>
            </li>
            <li><span><strong class="text-danger-emphasis">Ekip Çalışması</strong>: Diğer masumlarla birlikte hareket edin.</span>
            </li>
            <li><span><strong class="text-primary-emphasis">Soru Sorun</strong>: Diğer oyuncuların davranışlarını gözlemleyin ve sorular sorun.</span>
            </li>
        </ul>
        <br>

        <h4 class="tttdedektif">Dedektifler için:</h4>
        <ul style="list-style-type: '- '">
            <li><span><strong class="text-success-emphasis">Donanım Kullanımı</strong>: Dedektif olarak özel eşyalarınızı etkili bir şekilde
                kullanın.</span>
            </li>
            <li><span><strong class="text-danger-emphasis">Şüpheli Tespiti</strong>: Oyuncuların davranışlarını analiz edin. Masumlar ve
                teröristler arasındaki farkları görün.</span>
            </li>
            <li><span><strong class="text-warning-emphasis">Savunma Yapın</strong>: Masumları korumak için stratejik yerlerde durun.</span>
            </li>
        </ul>
        <br>

        <h4 class="ttthain">Teröristler için:</h4>
        <ul style="list-style-type: '* '">
            <li><span><strong class="text-success-emphasis">Kandırma</strong>: Diğer oyuncuları manipüle etmeye çalışın. Kendinizi masum gibi
                gösterin.</span>
            </li>
            <li><span><strong class="text-primary-emphasis">İşbirliği</strong>: Diğer teröristlerle iletişim kurarak birlikte hareket edin.</span>
            </li>
            <li><span><strong class="text-danger-emphasis">Hızlı Hareket</strong>: Dedektiflerden uzak durun ve hızlı hareket edin.</span>
            </li>
        </ul>
        <br>
        <h4 class="standartcolor">Öneriler</h4>
        <ul style="list-style-type: '+ '">
            <li><span><strong class="text-primary-emphasis">Sesli İletişim</strong>: Diğer oyuncularla iletişimde bulunmak için sesli chat kullanın;
                bu, stratejik kararlar almanıza yardımcı olabilir.</span>
            </li>
            <li><span><strong class="text-success-emphasis">Harita Bilgisi</strong>: Oynadığınız haritayı iyi öğrenin; bu, kaçış yolları ve saklanma
                yerleri bulmanıza yardımcı olur.</span>
            </li>
            <li><span><strong class="text-warning-emphasis">Oyuncu Davranışları</strong>: Diğer oyuncuların alışkanlıklarını gözlemleyin.
                Alışılmadık davranışlar genellikle şüphe uyandırır.</span>
            </li>
        </ul>
